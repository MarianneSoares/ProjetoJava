public class Pacientes {

    public String nomePaciente;
    public String IDPaciente;
    public String PlanoPaciente;
    public int numeroPaciente;

    public String getIDPaciente() {
        return IDPaciente;
    }

    public void setIDPaciente(String IDPaciente) {
        this.IDPaciente = IDPaciente;
    }

    public String getPlanoPaciente() {
        return PlanoPaciente;
    }

    public void setPlanoPaciente(String planoPaciente) {
        PlanoPaciente = planoPaciente;
    }

    public int getNumeroPaciente() {
        return numeroPaciente;
    }

    public void setNumeroPaciente(int numeroPaciente) {
        this.numeroPaciente = numeroPaciente;
    }

    public String getNomePaciente() {
        return nomePaciente;
    }

    public void setNomePaciente(String nomePaciente) {
        this.nomePaciente = nomePaciente;
    }
}
